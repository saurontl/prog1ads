#4 - Calcule a área de um retângulo (base x altura).
base = float(input('Digite o tamanho da base em centímetros: '))
altura = float(input('Digite o tamanho da altura em centímetros: '))
ret = base * altura
print('A área do retângulo é de {}cm.'.format(ret))
